def build_user_prompt(
    name: str,
    birth_date: str,
    birth_time: str | None,
    birth_place: str,
) -> str:
    time_part = f"Время рождения: {birth_time}" if birth_time else "Время рождения: не указано (используй солнечный знак без асцендента)"
    return (
        f"Составь персональный натальный гороскоп для:\n"
        f"Имя: {name}\n"
        f"Дата рождения: {birth_date}\n"
        f"{time_part}\n"
        f"Место рождения: {birth_place}\n\n"
        f"Обращайся к {name} по имени. Следуй структуре: БОЛЬ → ПОНИМАНИЕ → ОБЪЯСНЕНИЕ → НАДЕЖДА → ДЕЙСТВИЕ."
    )
